package xml;

public class TradingItemDetail {
	private String Id;
	private String Name;
	private String DataVendorId;
	
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getDataVendorId() {
		return DataVendorId;
	}
	public void setDataVendorId(String dataVendorId) {
		DataVendorId = dataVendorId;
	}
}
